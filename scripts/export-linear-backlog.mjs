function issue(input) {
  const labels = unique([
    ...input.labels,
    `priority/${input.priority}`,
    input.mvp === false ? null : "mvp-required",
  ].filter(Boolean));

  return {
    "Task ID": input.id,
    Title: input.title,
    Area: input.area,
    Epic: input.epic,
    Priority: input.priority,
    "MVP Required": input.mvp === false ? "No" : "Yes",
    Effort: input.effort,
    "Risk Level": input.risk,
    Status: input.status,
    Milestone: input.milestone,
    Description: input.description,
    "User Value": input.userValue,
    "Current State": input.currentState,
    "Gap / Risk": input.gapRisk,
    Scope: input.scope,
    "Explicit Non-Goals": input.nonGoals,
    Dependencies: input.dependencies,
    "Definition of Done": input.dod,
    "Acceptance Tests": input.tests,
    "Validation Commands": input.commands,
    "Files Likely Affected": input.files,
    "Suggested Codex Planning Prompt": input.planPrompt,
    "Suggested Codex Implementation Prompt": input.implPrompt,
    "Recommended Branch Name": input.branch,
    Labels: labels,
    Source: input.source,
    Notes: input.notes,
    linear_sync: input.sync,
  };
}

function unique(values) {
  return Array.from(new Set(values));
}

async function writeJson(path, value) {
  await writeFile(path, `${JSON.stringify(value, null, 2)}\n`);
}

function countObject(items, field) {
  return items.reduce((counts, item) => {
    const key = item[field];
    counts[key] = (counts[key] || 0) + 1;
    return counts;
  }, {});
}

function countLines(items, field) {
  const counts = countObject(items, field);
  return Object.keys(counts).sort().map((key) => `- ${key}: ${counts[key]}`);
}
